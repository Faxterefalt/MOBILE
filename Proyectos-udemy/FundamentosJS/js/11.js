//Iteradores en arrays
const tecnologías = ['HTML', 'CSS', 'JavaScript', 'React', 'Node.js']

//forEach - acceder a cada elemento
const arrayForeach = tecnologías.forEach(function(i){
    return i
})

//map - crea un nuevo array
const arrayMap = tecnologías.map(function(i){
    return i
})

console.log(arrayForeach)
console.log(arrayMap)